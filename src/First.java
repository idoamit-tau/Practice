public class First {
    public static void main(String[] args) {
        // Learning Arithmetic Operators.
        int a = 12;
        int b = a++;
        int c = 12;
        int d = ++c;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println("-------------------");

        a = 8;
        b = 16;
        a = a<<8;
        System.out.println(a);

    }
}
